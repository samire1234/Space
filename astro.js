document.addEventListener("scroll", function () {
  console.log(window.scrollY);
 
  if (window.scrollY > 250) {
    document.querySelectorAll(".yazi5 img").forEach(x => {
      x.style.animationName = "animasiya5"
    });
  }
  else {
    document.querySelectorAll(".yazi5 img").forEach(x => {
      x.style.animationName = ""
    });
  }
})

